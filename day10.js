function parseData(indata) {
    return indata.trim().split("\n");
}


function getPointsForRow(row) {

    let close1 = row.indexOf(')'),
        close2 = row.indexOf(']'),
        close3 = row.indexOf('}'),
        close4 = row.indexOf('>'),
        point = 0,
        pos = Number.MAX_VALUE;

    if (close1 !== -1 && close1 < pos) {
        point = 3;
        pos = close1;
    }
    if (close2 !== -1 && close2 < pos) {
        point = 57;
        pos = close2;
    }
    if (close3 !== -1 && close3 < pos) {
        point = 1197;
        pos = close3;
    }
    if (close4 !== -1 && close4 < pos) {
        point = 25137;
        pos = close4;
    }
    
    return point;
}

function getAutoCompletePoints(row) {

    let chars = row.split('');
        points = 0;

    chars.reverse().forEach((char, i) => {

        points = points * 5;

        if(char === '(') {
            points += 1;
        } else if(char === '[') {
            points += 2;
        } else if(char === '{') {
            points += 3;
        } else if(char === '<') {
            points += 4;
        }

    });

    return points;

}

function arraySort(arr) {
    arr.sort(function(a, b) {
        return a - b;
    });

    return arr;
}

function partCalc(input) {

    let data = parseData(input),
        totalPoints = 0,
        totalAutoPoints = [];
    
    
    data.forEach((row, ri) => {

        while (
            row.indexOf('()') !== -1 ||
            row.indexOf('[]') !== -1 ||
            row.indexOf('{}') !== -1 ||
            row.indexOf('<>') !== -1
        ) {
            row = row.replace(/\(\)/g, "");
            row = row.replace(/\[\]/g, "");
            row = row.replace(/\{\}/g, "");
            row = row.replace(/\<\>/g, "");
        }


        let points = getPointsForRow(row);

        if(points === 0) {
            totalAutoPoints.push(getAutoCompletePoints(row));
        }

        totalPoints += points;

    });
    
    totalAutoPoints = arraySort(totalAutoPoints);
    console.log(totalAutoPoints);
    
    let middleScore = totalAutoPoints[Math.floor(totalAutoPoints.length/2)];

    return {
        totalPoints: totalPoints,
        middleScore: middleScore
    };

}

