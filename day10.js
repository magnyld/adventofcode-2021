function getPointsForRow(row) {

    let close1 = row.indexOf(')'),
        close2 = row.indexOf(']'),
        close3 = row.indexOf('}'),
        close4 = row.indexOf('>'),
        point = 0,
        pos = Number.MAX_VALUE;

    if (close1 !== -1 && close1 < pos) {
        point = 3;
        pos = close1;
    }
    if (close2 !== -1 && close2 < pos) {
        point = 57;
        pos = close2;
    }
    if (close3 !== -1 && close3 < pos) {
        point = 1197;
        pos = close3;
    }
    if (close4 !== -1 && close4 < pos) {
        point = 25137;
        pos = close4;
    }
    
    return point;
}

